    private void btAdd2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAdd2ActionPerformed
        // TODO add your handling code here:
        String ma = txtMa.getText();
        String ten = txtTen.getText();
        KieuDang kd = new KieuDang(ten, ma);
        JOptionPane.showMessageDialog(this, kds.Add(kd));
        listDg.add(kd);
        showData(listDg);
    }//GEN-LAST:event_btAdd2ActionPerformed

    private void btXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btXoaActionPerformed
        // TODO add your handling code here:
        String ma = listDg.get(jTable1.getSelectedRow()).getMakieu();
        JOptionPane.showMessageDialog(this, kds.delete(ma));
        listDg = kds.getAll();
        showData(listDg);
    }//GEN-LAST:event_btXoaActionPerformed

