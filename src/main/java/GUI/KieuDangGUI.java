    public void showData(List<KieuDang> list){
        dtm.setRowCount(0);
        for (KieuDang deGiayJPN : list) {
            dtm.addRow(new Object[] {deGiayJPN.getMakieu(), deGiayJPN.getTenkieu()});
        }
    }
